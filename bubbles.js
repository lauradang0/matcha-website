const container = document.getElementById('bubbles');
const vw = window.innerWidth;
const vh = window.innerHeight;
const designW = 1440;
const designH = 900;

bubbleData.forEach(([x, y, size]) => {
  const dot = document.createElement('div');
  dot.className = 'bubble';
  const scaledSize = size * Math.min(vw / designW, vh / designH);
  const scaledX = (x / designW) * vw;
  const scaledY = (y / designH) * vh;
  dot.style.width = scaledSize + 'px';
  dot.style.height = scaledSize + 'px';
  dot.style.left = scaledX + 'px';
  dot.style.top = scaledY + 'px';
  container.appendChild(dot);
});
